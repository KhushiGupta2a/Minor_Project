        <br><br><br>
                <div class="card-3">
                         <div class="we">
                                    <p style="text-align:justify; color:white; font-size:28px;">Impersonation and identity theft is an act of fraudulently or dishonestly making use of the electronic signature, password or any other unique identification feature of any other person.</p>
                         </div>
                </div><br>

<div class="four">
    <b> <p p style="text-align:left; color:black; font-size:32px;">4. PHISHING</p></b>
</div>
<br><br><br>
<div class="card-4">
             <div class="for">
                    <p style="text-align:justify; color:white; font-size:28px;">Phishing is a type of fraud that involves stealing personal information such as Customer ID, IPIN, Credit/Debit Card number, Card expiry date, CVV number, etc. through emails that appear to be from a legitimate source.</p>
             </div>
</div><br>
        
<div class="five">
    <b> <p p style="text-align:left; color:black; font-size:32px;">5. VISHING</p></b>
</div>
<br><br><br>
<div class="card-5">
             <div class="he">
                    <p style="text-align:justify; color:white; font-size:28px;">Vishing is an attempt where fraudsters try to seek personal information like Customer ID, Net Banking password, ATM PIN, OTP, Card expiry date, CVV etc. through a phone call.</p>
             </div>
</div><br>
        
<div class="six">
    <b> <p p style="text-align:left; color:black; font-size:32px;">6. SMISHING</p></b>
</div>
<br><br><br>
<div class="card-6">
             <div class="it">
                    <p style="text-align:justify; color:white; font-size:28px;">Smishing is a type of fraud that uses mobile phone text messages to lure victims into calling back on a fraudulent phone number, visiting fraudulent websites or downloading malicious content via phone or web.</p>
             </div>
</div><br>


