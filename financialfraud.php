    <b> <p p style="text-align:left; color:black; font-size:32px;">2. SIM SWAP SCAM</p></b>
</div>
<br><br><br>
        <div class="card-2">
                 <div class="you">
                            <p style="text-align:justify; color:white; font-size:28px;">SIM Swap Scam occurs when fraudsters manage to get a new SIM card issued against a registered mobile number fraudulently through the mobile service provider. With the help of this new SIM card, they get One Time Password (OTP) and alerts, required for making financial transactions through victim's bank account. Getting a new SIM card against a registered mobile number fraudulently is known as SIM Swap.</p>
                 </div>
</div><br>

